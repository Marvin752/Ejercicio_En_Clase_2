package umg.programacion2.PKG_Grupo2;

public class ClsGrupo2 {
    public float triarea(float base, float altura) {
        return (base*altura)/2;
    }
    public float piravol(float base, float altura)
    {
        return (base*altura)/3;
    }
    public float repiarea(float base, float altura) {
        return (base*altura)/2;
    }
}
